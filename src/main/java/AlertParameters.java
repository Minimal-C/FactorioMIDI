public class AlertParameters {
	private boolean show_alert;
	private boolean show_on_map;
	private String alert_message;

	public AlertParameters(boolean show_alert, boolean show_on_map, String alert_message) {
		this.show_alert = show_alert;
		this.show_on_map = show_on_map;
		this.alert_message = alert_message;
	}
}
